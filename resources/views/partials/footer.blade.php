        document.addEventListener("DOMContentLoaded", function () {
            const footer = document.getElementById("serbu-footer");
            const items = footer.querySelectorAll(".reveal-item");

            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        // Loop setiap elemen di dalam footer dengan delay bertahap
                        items.forEach((item, index) => {
                            setTimeout(() => {
                                item.classList.remove("opacity-0", "translate-y-8");
                                item.classList.add("opacity-100", "translate-y-0");
                            }, index * 200); // Muncul setiap 0.2 detik bergantian
                        });
                        // Berhenti mengamati setelah animasi jalan
                        observer.unobserve(footer);
                    }
                });
            }, { threshold: 0.2 });

            observer.observe(footer);
        });
